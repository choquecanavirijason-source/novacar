export const dictionaries: Record<Locale, Dict> = { es, en };

/** Lee una ruta con punto de un objeto anidado. */
function getPath(obj: Dict, path: string): string | undefined {
  const value = path.split(".").reduce<unknown>((acc, key) => {
    if (acc && typeof acc === "object") return (acc as Dict)[key];
    return undefined;
  }, obj);
  return typeof value === "string" ? value : undefined;
}

export type TranslateVars = Record<string, string | number>;

/** Traduce una clave para un locale, con fallback a ES y a la propia clave. */
export function translate(locale: Locale, key: string, vars?: TranslateVars): string {
  let text = getPath(dictionaries[locale], key) ?? getPath(dictionaries.es, key) ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return text;
}
